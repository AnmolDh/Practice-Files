public class multipleAttrib {
    String fname = "Anmol";
    String lname = "Dhiman";
    int age = 19;

    public static void main(String[] args) {
        multipleAttrib myObj = new multipleAttrib();
        System.out.println("Name: " + myObj.fname + " " + myObj.lname);
        System.out.println("Age: " + myObj.age);
    }
}
