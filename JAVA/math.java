package JAVA;

public class math {
    public static void main(String[] args){
        System.out.println(Math.max(2, 4));
        System.out.println(Math.min(2, 4));
        System.out.println(Math.sqrt(64));
        System.out.println(Math.abs(-4.7));
        System.out.println(Math.random() * 101);
    }
}
